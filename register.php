    function alert(type, msg, position='body') {
        let bs_class = (type == 'success') ? 'alert-success' : 'alert-danger';
        let element = document.createElement('div');
        element.innerHTML = `
            <div class="alert ${bs_class} alert-dismissible fade show alert-container" role="alert" id="autoDismissAlert">
                <strong class="me-3">${msg}</strong>
                <button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="Close"></button>
            </div>
        `; 
        if(position=='body') {
            document.body.append(element);
            // element.classList.add('alert-container');
        } else {
            document.getElementById(position).appendChild(element);
        }

        // Automatically close after 2 seceonds
        setTimeout(() => {
            let alertEl = element.querySelector('.alert');
            if(alertEl) {
                let bsAlert = bootstrap.Alert.getOrCreateInstance(alertEl);
                bsAlert.close();
            }
        }, 5000);
    }

    
    let register_form = document.getElementById('register-form');
    let registerSubmitBtn = document.getElementById('registerSubmitBtn');
    let registerSpinner = document.getElementById('registerSpinner');
    let registerBtnText = registerSubmitBtn.querySelector(".btn-text");

    register_form.addEventListener('submit', (e) => {
        e.preventDefault();

        // Disable + show spinner
        registerSubmitBtn.disabled = true;
        registerSpinner.style.display = "inline-block";
        registerBtnText.textContent = "Please wait...";

        let data = new FormData();
        data.append('name', register_form.elements['name'].value);
        data.append('email', register_form.elements['email'].value);
        data.append('phonenum', register_form.elements['phonenum'].value);
        data.append('address', register_form.elements['address'].value);
        data.append('dob', register_form.elements['dob'].value);
        data.append('zipcode', register_form.elements['zipcode'].value);
        data.append('password', register_form.elements['password'].value);
        data.append('cpassword', register_form.elements['cpassword'].value);
        data.append('profile', register_form.elements['profile'].files[0]);
        data.append('register', '');

        let xhr = new XMLHttpRequest();
        xhr.open("POST", "ajax/login_register.php", true);

        xhr.onload = function(){
            // Re-enable button and hide spinner
            registerSubmitBtn.disabled = false;
            registerSpinner.style.display = "none";
            registerBtnText.textContent = "Register";

            if(this.responseText == 'password_mismatch') {
                alert('error', "Password does not match");
            } else if(this.responseText == 'email_already') {
                alert('error', "Email address taken!");
            } else if(this.responseText == 'phone_already') {
                alert('error', "Phone number taken!");
            } else if(this.responseText == 'inv_img') {
                alert('error', 'Only JPG, PNG or WEBP images are allowed!');
            } else if(this.responseText == 'Upload Failed') {
                alert('error', 'Image upload failed. Server Down!');
            } else if(this.responseText == 'Upload Failed') {
                alert('error', 'Image upload failed. Server Down!');
            } else if(this.responseText == 'mail_failed') {
                alert('error', 'Cannot send confirmation email! Server Down!');
            } else if(this.responseText == 'ins_failed') {
                alert('error', 'Registration failed! Server Down!');
            } else {
                alert('success', "Registration successful. Confirmation link sent to email!");
                register_form.reset();
            }
        }

        xhr.send(data);
    });
</script>

